/**
 * XSS Detection Patterns — HTML script tags and event handlers
 */
const XSS_PATTERNS: RegExp[] = [
  /<script\b[^<]*(?:(?!<\/script>)<[^<]*)*<\/script>/gi,
  /<script[^>]*>/gi,
  /<\/script>/gi,
  /javascript\s*:/gi,
  /on\w+\s*=\s*["'][^"']*["']/gi,  // onclick="...", onload="..."
  /on\w+\s*=\s*[^\s>]*/gi,           // onclick=alert()
  /<iframe\b/gi,
  /<object\b/gi,
  /<embed\b/gi,
  /<form\b/gi,
  /expression\s*\(/gi,  // CSS expression()
  /vbscript\s*:/gi,
  /data\s*:\s*text\/html/gi,
];

/**
 * SQL Injection Detection Patterns
 */
const SQL_INJECTION_PATTERNS: RegExp[] = [
  /union\s+select/gi,
  /union\s+all\s+select/gi,
  /or\s+1\s*=\s*1/gi,
  /and\s+1\s*=\s*1/gi,
  /'\s*or\s+'[^']*'\s*=\s*'/gi,
  /'\s*;\s*drop\s+/gi,
  /'\s*;\s*delete\s+/gi,
  /'\s*;\s*update\s+/gi,
  /'\s*;\s*insert\s+/gi,
  /--\s*$/gm,  // SQL comment terminator
  /\/\*.*\*\//gi,  // SQL block comment
  /xp_cmdshell/gi,
  /exec\s*\(/gi,
  /execute\s*\(/gi,
  /information_schema/gi,
  /sysobjects/gi,
  /syscolumns/gi,
];

