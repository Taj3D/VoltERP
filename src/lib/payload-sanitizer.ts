export interface SanitizationResult {
  sanitized: boolean;
  threats: string[];
  cleanedPayload: any;
}

/**
 * sanitizeString — Strips XSS script tags and detects SQL injection in a string value
 */
function sanitizeString(value: string): { cleaned: string; threats: string[] } {
  const threats: string[] = [];
  let cleaned = value;

  // Check XSS patterns
  for (const pattern of XSS_PATTERNS) {
    if (pattern.test(cleaned)) {
      threats.push(`XSS_DETECTED: Pattern "${pattern.source}" found in value`);
      // Strip the malicious content
      cleaned = cleaned.replace(pattern, '');
    }
  }

  // Check SQL injection patterns
  for (const pattern of SQL_INJECTION_PATTERNS) {
    if (pattern.test(cleaned)) {
      threats.push(`SQL_INJECTION_DETECTED: Pattern "${pattern.source}" found in value`);
      // Remove the SQL injection attempt
      cleaned = cleaned.replace(pattern, '');
    }
  }

  // Enforce max length
  if (cleaned.length > MAX_FIELD_LENGTH) {
    cleaned = cleaned.substring(0, MAX_FIELD_LENGTH);
    threats.push(`FIELD_TRUNCATED: Value exceeded maximum length of ${MAX_FIELD_LENGTH}`);
  }

  return { cleaned, threats };
}

/**
 * sanitizePayload — Recursively sanitizes an entire JSON payload
 * Strips XSS, SQL injection, unauthorized nested properties
 * Enforces maximum nesting depth
 */
export function sanitizePayload(
  payload: any,
  depth: number = 0,
  path: string = 'root'
): SanitizationResult {
  const threats: string[] = [];

  // Null/undefined/primitive handling
  if (payload === null || payload === undefined) {
    return { sanitized: false, threats: [], cleanedPayload: payload };
  }

  if (typeof payload === 'string') {
    const result = sanitizeString(payload);
    return { sanitized: result.threats.length > 0, threats: result.threats, cleanedPayload: result.cleaned };
  }

  if (typeof payload === 'number' || typeof payload === 'boolean') {
    return { sanitized: false, threats: [], cleanedPayload: payload };
  }

  // Array handling
  if (Array.isArray(payload)) {
    const cleanedArray: any[] = [];
    for (let i = 0; i < payload.length; i++) {
      const result = sanitizePayload(payload[i], depth + 1, `${path}[${i}]`);
      threats.push(...result.threats);
      cleanedArray.push(result.cleanedPayload);
    }
    return { sanitized: threats.length > 0, threats, cleanedPayload: cleanedArray };
  }

  // Object handling
  if (typeof payload === 'object') {
    // Enforce nesting depth
    if (depth > MAX_NESTING_DEPTH) {
      threats.push(`NESTING_DEPTH_EXCEEDED: Object at "${path}" exceeds max depth of ${MAX_NESTING_DEPTH}`);
      return { sanitized: true, threats, cleanedPayload: {} };
    }

    const cleanedObj: Record<string, any> = {};

    for (const [key, value] of Object.entries(payload)) {
      // Strip unauthorized nested properties (at top level, allow common + custom keys)
      // At deeper levels, be more permissive but still sanitize values
      if (depth === 0 && !ALLOWED_COMMON_FIELDS.has(key) && !key.startsWith('_') && !key.match(/^[a-z][a-zA-Z0-9]*$/)) {
        threats.push(`UNAUTHORIZED_PROPERTY: Property "${key}" at "${path}" is not in the allowed field list`);
        continue; // Skip this property
      }

      // Recursively sanitize the value
      const result = sanitizePayload(value, depth + 1, `${path}.${key}`);
      threats.push(...result.threats);
      cleanedObj[key] = result.cleanedPayload;
    }

    return { sanitized: threats.length > 0, threats, cleanedPayload: cleanedObj };
  }

  return { sanitized: false, threats: [], cleanedPayload: payload };
}

/**
 * logSecurityThreat — Records a detected security threat to the SecurityThreatLog table
 */
export async function logSecurityThreat(params: {
  threatType: string;
  ipAddress?: string;
  userAgent?: string;
  endpoint?: string;
  httpMethod?: string;
  payload?: string;
  blockedAction?: string;
  severity?: string;
  companyId?: string;
  userId?: string;
}): Promise<void> {
  try {
    const { db } = await import('@/lib/db');
    await db.securityThreatLog.create({
      data: {
        threatType: params.threatType,
        ipAddress: params.ipAddress || null,
        endpoint: params.endpoint || null,
        payload: params.payload ? String(params.payload).substring(0, 5000) : null,
        notes: [params.userAgent, params.httpMethod, params.blockedAction].filter(Boolean).join(' | ') || null,
        severity: params.severity || 'HIGH',
        companyId: params.companyId || null,
      },
    });
  } catch (error) {
    console.error('[SecurityThreatLogger] Failed to log threat:', error);
  }
}
